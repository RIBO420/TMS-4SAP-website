type UiKeys = keyof typeof ui.en;

export function getLangFromUrl(url: URL): Lang {
  const [, lang] = url.pathname.split('/');
  if (lang === 'nl' || lang === 'de') return lang;
  return defaultLang;
}

export function getLocalizedPath(path: string, lang: Lang): string {
  if (lang === defaultLang) return path;
  // Strip any existing lang prefix first
  const cleaned = path.replace(/^\/(en|nl|de)(\/|$)/, '/');
  if (cleaned === '/') return `/${lang}`;
  return `/${lang}${cleaned}`;
}

export function t(lang: Lang, key: string): string {
  const translations = ui[lang];
  return (translations as Record<string, string>)[key] ?? (ui[defaultLang] as Record<string, string>)[key] ?? key;
}
